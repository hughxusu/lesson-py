message = 'hello, world'
print(max(message))
print(min(message))

nums = [10, 20, 30, 40]
print(max(nums))
print(min(nums))

nums = set(nums)
print(max(nums))
print(min(nums))

letters = {'a': 1, 'b': 2, 'c': 3}
print(max(letters))
print(max(letters.values()))
print(min(letters))
print(min(letters.values()))